def bubble_sort(numbers):
    """
    Функція сортує список чисел за допомогою бульбашкового сортування.

    Параметри:
    numbers (list): Список чисел, який потрібно відсортувати.

    Повертає:
    list: Відсортований список чисел.
    """
    # Проходимо по всіх елементах списку
    for i in range(len(numbers)):
        # На кожному етапі найбільший елемент спливає до кінця списку
        for j in range(0, len(numbers) - i - 1):
            # Якщо поточний елемент більший за наступний, міняємо їх місцями
            if numbers[j] > numbers[j + 1]:
                # Обмін значень
                numbers[j], numbers[j + 1] = numbers[j + 1], numbers[j]

    # Повертаємо відсортований список
    return numbers


# Ініціалізуємо список для сортування
unsorted_list = [64, 34, 25, 12, 22, 11, 90]

# Викликаємо функцію сортування та виводимо результат
sorted_list = bubble_sort(unsorted_list)
print(f"Відсортований список: {sorted_list}")