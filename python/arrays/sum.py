# Вхідний список
numbers = [5, 10, 15]

# Обчислення суми елементів
sum_of_numbers = sum(numbers)

print("Сума елементів у списку:", sum_of_numbers)

# Ініціалізація змінної для суми
sum_of_numbers = 0

# Цикл для обчислення суми елементів
for num in numbers:
    sum_of_numbers += num

print("Сума елементів у списку:", sum_of_numbers)
